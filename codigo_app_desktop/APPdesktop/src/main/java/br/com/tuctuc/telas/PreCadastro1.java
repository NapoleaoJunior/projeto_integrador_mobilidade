    private void campoSalvarCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_campoSalvarCadastroActionPerformed
        // salvar dados
        BD banco = new BD();
        boolean resultado = banco.conectaBD();
        String query ="INSERT INTO cliente(nome_usuario,cpf,celular,email,senha_login)VALUES (?,?,?,?,?);";
        PreparedStatement ps = banco.getPreparedStatement(query);
        try {
            ps.setString(1, campoUserName.getText());
            ps.setString(2, campoCpf.getText());
            ps.setString(3, campoCelular.getText());
            ps.setString(4, campoEmail.getText());
            ps.setString(5, campoSenha.getText());
            
            ps.execute();
            
            this.limpaCampos();
            
            JOptionPane.showMessageDialog(this, "Usuario inserido");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Houve um erro ao salvar os Dados" + ex.getMessage());
        }

    }//GEN-LAST:event_campoSalvarCadastroActionPerformed

